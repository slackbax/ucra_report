/**
 * @param $d
 * @return array
 */
function getArrayDate($d): array
{
  $date = strtotime($d);
  $week_days = array("Domingo", "Lunes", "Martes", "Miercoles", "Jueves", "Viernes", "Sábado");
  $months = array("Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio", "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre");

  $aux_d = date("w", $date);
  $day_w = $week_days[$aux_d];

  $day = date("d", $date);

  $aux_m = date("n", $date);
  $month_w = $months[$aux_m - 1];

  $month = date("m", $date);

  $year = date("Y", $date);
  return array('day_w' => $day_w, 'day' => $day, 'month_w' => $month_w, 'month' => $month, 'year' => $year);
}

/**
 * @param $arr
 * @param int $decs
 * @return float|int
 */
function mid($arr, int $decs = 2): float|int
{
  return round((array_sum($arr) / count($arr)), $decs);
}

/**
 * @param $arr
 * @return mixed
 */
function median($arr): mixed
{
  sort($arr, SORT_NUMERIC);
  $l = count($arr);
  return ($l % 2 == 0) ? ($arr[$l / 2 - 1] + $arr[$l / 2]) / 2 : $arr[floor($l / 2)];
}

/**
 * @param array $a
 * @param int $decs
 * @param bool $sample
 * @return float|bool
 */
function standard_deviation(array $a, int $decs = 2, bool $sample = false): float|bool
{
  $n = count($a);
  if ($n === 0) {
    trigger_error("The array has zero elements", E_USER_WARNING);
    return false;
  }
  if ($sample && $n === 1) {
    trigger_error("The array has only 1 element", E_USER_WARNING);
    return false;
  }
  $mean = array_sum($a) / $n;
  $carry = 0.0;
  foreach ($a as $val) {
    $d = ((double)$val) - $mean;
    $carry += $d * $d;
  };
  if ($sample) {
    --$n;
  }
  return round(sqrt($carry / $n), $decs);
}

/**
 * @param $dir
 * @return bool
 */
function delTree($dir): bool
{
  $files = array_diff(scandir($dir), array('.','..'));
  foreach ($files as $file) {
    (is_dir("$dir/$file")) ? delTree("$dir/$file") : unlink("$dir/$file");
  }
  return rmdir($dir);
}